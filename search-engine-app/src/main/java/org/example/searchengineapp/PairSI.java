package org.example.searchengineapp;

public class PairSI
{
    String first;
    Integer sec;

    public PairSI(String f,Integer s)
    {
        first=f;
        sec=s;
    }

    public String getfirst()
    {
        return first;
    }
    public Integer getsecond()
    {
        return sec;
    }
}
